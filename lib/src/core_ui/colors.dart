extension ThemeColorUtils on CupertinoDynamicColor {
  Color resolveWithTheme(ThemeData theme, {bool isHighContrast = false}) {
    final lerpValue = theme.interpolationValue;
    return isHighContrast
        ? Color.lerp(highContrastColor, darkHighContrastColor, lerpValue)!
        : Color.lerp(color, darkColor, lerpValue)!;
  }

  Color resolveWithBrightness(Brightness brightness) {
    return brightness == Brightness.light ? color : darkColor;
  }

  Color reverseResolveWithTheme(
    ThemeData theme, {
    bool isHighContrast = false,
  }) {
    final modifiedTheme = theme.copyWith(
      brightness:
          theme.brightness == Brightness.light
              ? Brightness.dark
              : Brightness.light,
    );
    return resolveWithTheme(modifiedTheme, isHighContrast: isHighContrast);
  }

  CupertinoDynamicColor map(Color Function(Color color) fn) {
    return CupertinoDynamicColor(
      color: fn(color),
      darkColor: fn(darkColor),
      highContrastColor: fn(highContrastColor),
      darkHighContrastColor: fn(darkHighContrastColor),
      elevatedColor: fn(elevatedColor),
      darkElevatedColor: fn(darkElevatedColor),
      highContrastElevatedColor: fn(highContrastElevatedColor),
      darkHighContrastElevatedColor: fn(darkHighContrastElevatedColor),
    );
  }

  CupertinoDynamicColor mapWithBrightness(
    Color Function(Color color, Brightness brightness) fn,
  ) {
    return CupertinoDynamicColor(
      color: fn(color, Brightness.light),
      darkColor: fn(darkColor, Brightness.dark),
      highContrastColor: fn(highContrastColor, Brightness.light),
      darkHighContrastColor: fn(darkHighContrastColor, Brightness.dark),
      elevatedColor: fn(elevatedColor, Brightness.light),
      darkElevatedColor: fn(darkElevatedColor, Brightness.dark),
      highContrastElevatedColor: fn(
        highContrastElevatedColor,
        Brightness.light,
      ),
      darkHighContrastElevatedColor: fn(
        darkHighContrastElevatedColor,
        Brightness.dark,
      ),
    );
  }
}

extension CoreColorUtils on Color {
  Color fade(double opacity) {
    return withValues(alpha: opacity, red: r, green: g, blue: b);
  }

  Color withOpacityFactor(double factor) {
    return withValues(
      alpha: (a * factor).clamp(0.0, 1.0),
      red: r,
      green: g,
      blue: b,
    );
  }

  Color invert() {
    return withValues(
      alpha: 1.0 - a,
      red: 1.0 - r,
      green: 1.0 - g,
      blue: 1.0 - b,
    );
  }
}
